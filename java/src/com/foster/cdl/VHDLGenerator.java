package com.foster.cdl;

public class VHDLGenerator
{

	public String visit(Tree node)
	{
		switch(node.nodetype)
		{
			case COMPONENT:
				break;
			case PORTDEC:
				break;
			case PORT:
				break;
			case ARCH:
				break;
			case GENDEC:
				break;
			case IDENTIFIER:
				break;
			case CONSTANT:
				break;
			case TERNARYOP:
				break;
			case BINARYOP:
				break;
			case UNARYOP:
				break;
		}
		return "";
	}
}
